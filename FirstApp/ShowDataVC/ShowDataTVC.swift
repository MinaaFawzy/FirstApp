import UIKit
import FirebaseDatabase

class ShowDataTVC: UITableViewController {	
        
    @IBOutlet var myTable: UITableView!
    var rep:DatabaseReference!
    var handle:DatabaseHandle!
    var myNames:[String] = []
    override func viewDidLoad() {
        super.viewDidLoad()
        let nibName = String(describing: "MyCell")
        let nib = UINib(nibName: nibName, bundle: nil)
        myTable.register(nib, forCellReuseIdentifier: nibName)
        
        rep = Database.database().reference()
        handle = rep.child("Name").observe(.childAdded, with: { x in
            if let item = x.value as? String {
                self.myNames.append(item)
                self.myTable.reloadData()
            }
        })
       	
    }

    // MARK: - Table view data source

